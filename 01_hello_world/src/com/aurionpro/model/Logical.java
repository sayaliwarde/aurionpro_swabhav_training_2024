package com.aurionpro.model;

public class Logical {
	public static void main(String[] args) {
	boolean a = true;
	boolean b = false;
	//logical and
	System.out.println("Logical AND operation(a&&b)\t"+(a&&b));
	//logical OR
	System.out.println("Logical OR operation(a||b)\t"+(a||b));
	//logical NOT
	System.out.println("Logical NOT operation(!a)\t"+(!a));	
	System.out.println("Logical NOT operation(!b)\t"+(!b));
	}

}
